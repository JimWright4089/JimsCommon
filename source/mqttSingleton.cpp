#include "mqttSingleton.h"

//----------------------------------------------------------------------------
//  Global and Static data
//----------------------------------------------------------------------------
std::shared_ptr<MQTT> MQTTSingleton::sInstance = nullptr;

//----------------------------------------------------------------------------
//  Purpose:
//   Return the singleton
//
//  Notes:
//
//----------------------------------------------------------------------------
MQTT &MQTTSingleton::getInstance()
{
  if (sInstance == nullptr) 
  {
    sInstance = std::shared_ptr<MQTT>(new MQTT());
  }
  return *sInstance;
}
