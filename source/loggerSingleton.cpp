#include "loggerSingleton.h"

//----------------------------------------------------------------------------
//  Global and Static data
//----------------------------------------------------------------------------
std::shared_ptr<Logger> LoggerSingleton::sInstance = nullptr;

//----------------------------------------------------------------------------
//  Purpose:
//   Return the singleton
//
//  Notes:
//
//----------------------------------------------------------------------------
std::shared_ptr<Logger> LoggerSingleton::getInstance()
{
  if (sInstance == nullptr) 
  {
    sInstance = std::shared_ptr<Logger>(new Logger());
  }
  return sInstance;
}
